#include <iostream> // Includes the input and output library (cout, endl)
using namespace std;  // Allows using standard names without writting std::

int main() {         // Start of the main function (progarm entry point)
    int a = 8;       // first integer variable assigned to value 8
    int b = 10;      // second integer variable assigned to value 10
    int sum = a + b; // third  variable that stores the sum of a and b 

    cout << sum << endl; // print the value stored in `sum` followed by a new line 

    return 0;  // Return to 0 to indicate successful program execution 
}   